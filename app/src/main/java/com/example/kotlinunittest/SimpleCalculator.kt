package com.example.kotlinunittest

class SimpleCalculator {
    // Fungsi add menerima dua parameter bertipe data Int, yaitu a dan b.
    // Fungsi ini menjumlahkan nilai a dan b, lalu mengembalikan hasilnya sebagai Int.
    fun add(a: Int, b: Int): Int {
        // Mengembalikan hasil penjumlahan dari parameter a dan b.
        return a + b
    }
}
